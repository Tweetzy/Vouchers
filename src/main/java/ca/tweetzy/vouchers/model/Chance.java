package ca.tweetzy.vouchers.model;

import lombok.experimental.UtilityClass;

@UtilityClass
public final class Chance {

	public boolean tryChance(final double chance) {
		if (chance >= 100D) return true;
		return Math.random() < chance / 100;
	}
}
